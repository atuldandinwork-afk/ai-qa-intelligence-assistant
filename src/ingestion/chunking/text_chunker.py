def chunk_text(text, chunk_size=600, overlap=120):
    """Simple sliding window chunker."""

    words = text.split()
    chunks = []
    start = 0

    while start < len(words):
        end = start + chunk_size
        chunk = " ".join(words[start:end])
        chunks.append(chunk)
        start += chunk_size - overlap

    return chunks
